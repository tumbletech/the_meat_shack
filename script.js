//counter for pork patties//
function increaseValue01() {
    var value = parseInt(document.getElementById('quantity-porkpatties').value, 10)
    value = isNaN(value) ? 0 : value;
    value++;
    document.getElementById('quantity-porkpatties').value = value;
}

function decreaseValue01() {
    var value = parseInt(document.getElementById('quantity-porkpatties').value, 10)
 
    value = isNaN(value) ? 0 : value;
    value < 1 ? value = 1 : '';
    value--;
    document.getElementById('quantity-porkpatties').value = value;
}

